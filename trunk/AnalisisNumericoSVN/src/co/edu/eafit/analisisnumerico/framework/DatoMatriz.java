package co.edu.eafit.analisisnumerico.framework;

/**
 * Representa una posicion de una matriz
 * @author Sebastian
 * @since 17/04/2010
 *
 */
public class DatoMatriz {

	private int fila;
	private int columna;
	private double valor;
	private int marca;
	
	public int getFila() {
		return fila;
	}
	public void setFila(int fila) {
		this.fila = fila;
	}
	public int getColumna() {
		return columna;
	}
	public void setColumna(int columna) {
		this.columna = columna;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public int getMarca() {
		return marca;
	}
	public void setMarca(int marca) {
		this.marca = marca;
	}
	
	

	
}
